const DEFAULT_ZONE = '5'

/**
 * Определяет климатическую зону по данным address из Nominatim.
 * Проверяет address.state, address.region, address.county.
 */
function getZoneFromNominatim(address) {
  if (!address) return null
  const candidates = [address.state, address.region, address.county]
  for (const candidate of candidates) {
    if (!candidate) continue
    if (NOMINATIM_TO_ZONE[candidate]) return NOMINATIM_TO_ZONE[candidate]
    // Попробовать частичное совпадение для составных названий
    const found = Object.keys(NOMINATIM_TO_ZONE).find(k =>
      candidate.includes(k) || k.includes(candidate)
    )
    if (found) return NOMINATIM_TO_ZONE[found]
  }
  return null
}

function getCoordsForRegion(region) {
  if (!region) return DEFAULT_COORDS
  return REGION_COORDS[region] ?? DEFAULT_COORDS
}

function getZoneForRegion(region) {
  if (!region) return DEFAULT_ZONE
  return REGION_ZONE[region] ?? DEFAULT_ZONE
}

module.exports = { getCoordsForRegion, getZoneForRegion, getZoneFromNominatim, REGION_COORDS, REGION_ZONE }
