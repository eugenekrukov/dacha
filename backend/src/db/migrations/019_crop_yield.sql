  WHEN 'Свёкла'                THEN 0.30
  WHEN 'Лук репчатый'          THEN 0.10
  WHEN 'Чеснок'                THEN 0.05
  WHEN 'Редис'                 THEN 0.03
  WHEN 'Салат листовой'        THEN 0.30
  WHEN 'Картофель'             THEN 0.80
  -- Зелень (002)
  WHEN 'Укроп'                 THEN 0.05
  WHEN 'Петрушка'              THEN 0.10
  WHEN 'Базилик'               THEN 0.10
  WHEN 'Кинза'                 THEN 0.05
  -- Ягоды (002)
  WHEN 'Клубника'              THEN 0.30
  WHEN 'Малина'                THEN 1.5
  -- Овощи/бахча/зелень (006)
  WHEN 'Тыква'                 THEN 5.0
  WHEN 'Патиссон'              THEN 3.0
  WHEN 'Капуста цветная'       THEN 1.0
