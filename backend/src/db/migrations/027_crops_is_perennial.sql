-- Migration 027: флаг многолетника у культур.
-- Многолетники (клубника, малина, смородина и т.п.) НЕ сажают каждый год — их график ухода
-- считается от начала текущего сезона, а не от давней даты посадки (см. effectivePlantedAt
-- в utils/todayLogic.js). Также используется в справочнике для разделения однолетние/многолетние.

ALTER TABLE crops ADD COLUMN IF NOT EXISTS is_perennial BOOLEAN NOT NULL DEFAULT false;

UPDATE crops SET is_perennial = true
WHERE name IN (
  'Клубника', 'Земляника', 'Малина', 'Смородина чёрная', 'Крыжовник',
  'Ревень', 'Щавель', 'Хрен', 'Мята', 'Тимьян', 'Лук-батун'
);

COMMENT ON COLUMN crops.is_perennial IS
  'Многолетник: посадка живёт несколько сезонов, график ухода сбрасывается каждый сезон.';
