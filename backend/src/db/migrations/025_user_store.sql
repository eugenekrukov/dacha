--   'rustore'         — платный гейт (триал → подписка ЮKassa / промо), 402 после истечения;
--   'gplay'|'samsung' — оплата из РФ невозможна → доступ БЕЗ 402-гейта, монетизация рекламой РСЯ;
--   NULL              — неизвестно → трактуется как платный гейт (грандфатеринг существующих
--                       пользователей, которые все на rustore-сборке).
-- Колонка наследует владельца таблицы users (ALTER existing) — смена владельца не нужна.
ALTER TABLE users ADD COLUMN IF NOT EXISTS store TEXT;
